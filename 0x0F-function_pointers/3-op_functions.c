#include <stdio.h>
/**
 * op_add - function
 * @a:par1
 * @b:par2
 * Return:int
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function
 * @a:par1
 * @b:par2
 * Return:int
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function
 * @a:par1
 * @b:par2
 * Return:int
 */
int op_mul(int a, int b)
{
        return (a * b);
}
/**
 * op_div - function
 * @a:par1
 * @b:par2
 * Return:int
 */
int op_div(int a, int b)
{
        return (a / b);
}
/**
 * op_mod - function
 * @a:par1
 * @b:par2
 * Return:int
 */
int op_mod(int a, int b)
{
        return (a % b);
}
